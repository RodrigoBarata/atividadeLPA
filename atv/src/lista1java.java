import java.util.Scanner;
public class lista1java {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n;
        double s=0;
        n=sc.nextInt();
        for(int i=1;i<=n;i++){
            s+=1.0/i;
        }
        System.out.print(s);





















    }
}
